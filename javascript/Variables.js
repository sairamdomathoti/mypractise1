//integer
var a = 10;
// printing value
console.log("a value :" + a);

// printing type of value
console.log("Type of a value : " + typeof(a));

// string
var b="Sairam";
console.log("b value :" + b);
console.log("Type of b value : " + typeof(b));

// boolean
var bool=true;
console.log("bool value :" + bool);
console.log("Type of bool value : " + typeof(bool));


// array
var arr=[1,2,3,4,5];
console.log("arr value :" + arr);
console.log("Type of arr value : " + arr.length);

// object
var obj={"id" :"101","name": "Sairam"};
console.log("obj value :" + obj);
console.log("obj value :" + JSON.stringify(obj));
console.log("obj value :"  + typeof(obj));
